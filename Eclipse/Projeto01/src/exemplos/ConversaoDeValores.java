package exemplos;

public class ConversaoDeValores {
	public static void main (String []args) {
		int meuInt = 9;
		double meuDouble = meuInt; //conversão de int para double
		
		System.out.println (meuInt); // numero 9
		System.out.println(meuDouble); // numero 9.0 por conta da conversão
		
	}
}
