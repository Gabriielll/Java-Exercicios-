package exemplos;

public class ConversaoDeValoresDoubleToInt {
	public static void main(String[]args) {
		 double valorDouble = 9.78d; //numero que irá ser convertido
		 int valorInteiro = (int) valorDouble; // aqui ele faz a conversão
		 
		 System.out.println (valorDouble);
		 System.out.println (valorInteiro);
		 
	} 

}
